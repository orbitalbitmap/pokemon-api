CREATE TABLE Pokemons (
  pokedexNumber INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  generationNumber VARCHAR(255) NOT NULL,
  fromId INT DEFAULT NULL,
  protected BOOLEAN NOT NULL,
  createdAt DATETIME DEFAUlT NOW(),
  updatedAt DATETIME DEFAULT NOW() ON UPDATE NOW(),
  deletedAt DATETIME,
  PRIMARY KEY(pokedexNumber),
  FOREIGN KEY(fromId) REFERENCES Pokemons(pokedexNumber)
);

CREATE TABLE Types (
  id INT auto_increment,
  name VARCHAR(255) NOT NULL,
  createdAt DATETIME DEFAUlT NOW(),
  updatedAt DATETIME DEFAULT NOW() ON UPDATE NOW(),
  deletedAt DATETIME,
  PRIMARY KEY(id)
);

CREATE TABLE AlternateForms (
  id INT NOT NULL,
  name VARCHAR(255) NOT NULL,
  createdAt DATETIME DEFAUlT NOW(),
  updatedAt DATETIME DEFAULT NOW() ON UPDATE NOW(),
  deletedAt DATETIME,
  PRIMARY KEY(id)
);

CREATE TABLE PokemonTypes (
  pokemonId INT NOT NULL,
  typeId INT NOT NULL,
  createdAt DATETIME DEFAUlT NOW(),
  updatedAt DATETIME DEFAULT NOW() ON UPDATE NOW(),
  deletedAt DATETIME,
  PRIMARY KEY(pokemonId, typeId),
  FOREIGN KEY(pokemonId) REFERENCES Pokemons(pokedexNumber),
  FOREIGN KEY(typeId) REFERENCES Types(id)
);

CREATE TABLE PokemonAlternateForms (
  pokemonId INT NOT NULL,
  formId INT NOT NULL,
  createdAt DATETIME DEFAUlT NOW(),
  updatedAt DATETIME DEFAULT NOW() ON UPDATE NOW(),
  deletedAt DATETIME,
  PRIMARY KEY(pokemonId, formId),
  FOREIGN KEY(pokemonId) REFERENCES Pokemons(pokedexNumber),
  FOREIGN KEY(formId) REFERENCES AlternateForms(id)
);


